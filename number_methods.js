// methods for numbers 

// ----------------------------toString() method------------------------------

// changing the type of number into an string

let num1 = 123;
console.log(num1.toString());

//-----------------------------toFixed() method------------------------------

// it is used to round the number into a fixed number of decimal places

let num2 = 3.14159;
console.log(num2.toFixed(2));

//-----------------------------precision() method------------------------------

//round off the total number of digit according to the given input 

let num3 = 3.14159;
console.log(num3.toPrecision(3));

//-----------------------------tolocaleString() method------------------------------

// it is used to convert a number into a string according to the local language
//(like adding comma in the number)

let num4 = 1234567.89;
console.log(num4.toLocaleString());



